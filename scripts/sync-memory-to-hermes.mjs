#!/usr/bin/env node
// Bridges Claude Code's dowiz operating rules + auto-memory index into Hermes
// Agent's memory surfaces. Deterministic text transform, no LLM call.
//
// Outputs:
//   1. HERMES.md (repo root)      — regenerated every run (derived artifact).
//      Hermes's context-file loader is first-match-wins across
//      .hermes.md/HERMES.md > AGENTS.md > CLAUDE.md, so without this file
//      Hermes would only ever see AGENTS.md and never CLAUDE.md's rules or
//      the Claude memory corpus.
//   2. ~/.hermes/memories/{MEMORY,USER}.md — seeded ONCE (skipped if already
//      present) since Hermes's own memory tool edits these organically after
//      first use; a re-run must not clobber what Hermes has since learned.
//
// Re-run after CLAUDE.md or the Claude memory MEMORY.md index changes:
//   node scripts/sync-memory-to-hermes.mjs

import { readFileSync, writeFileSync, existsSync, mkdirSync } from "node:fs";
import { homedir } from "node:os";
import { join } from "node:path";

const REPO_ROOT = "/root/dowiz";
const CLAUDE_MD_CORE = join(REPO_ROOT, ".claude/CLAUDE.md");
const CLAUDE_MEMORY_DIR = "/root/.claude/projects/-root-dowiz/memory";
const CLAUDE_MEMORY_INDEX = join(CLAUDE_MEMORY_DIR, "MEMORY.md");
const HERMES_MD_OUT = join(REPO_ROOT, "HERMES.md");
const HERMES_HOME = join(homedir(), ".hermes");
const HERMES_MEMORY_DIR = join(HERMES_HOME, "memories");

const CONTEXT_FILE_BUDGET = 18_000; // stay under Hermes's 20,000-char cap

function stripMarkdownLinks(line) {
  // "- [Title](file.md) — hook" -> "- Title — hook"
  // "[Title](../../foo.md)"     -> "Title"
  return line.replace(/\[([^\]]+)\]\([^)]+\)/g, "$1");
}

function transformMemoryIndex(raw) {
  return raw
    .split("\n")
    .map(stripMarkdownLinks)
    .join("\n");
}

function truncateToBudget(text, budget) {
  if (text.length <= budget) return text;
  const marker = "\n\n...[truncated by sync-memory-to-hermes.mjs to fit Hermes's context-file budget — read the full files via the terminal tool: " +
    CLAUDE_MD_CORE + ", " + CLAUDE_MEMORY_INDEX + "]\n";
  return text.slice(0, budget - marker.length) + marker;
}

function buildHermesMd() {
  const claudeCore = readFileSync(CLAUDE_MD_CORE, "utf8");
  const memoryIndex = transformMemoryIndex(readFileSync(CLAUDE_MEMORY_INDEX, "utf8"));

  const header = `# HERMES.md — Hermes Agent bridge for dowiz

> Auto-generated by \`scripts/sync-memory-to-hermes.mjs\` — do not hand-edit, re-run the
> script instead. Hermes loads exactly one project context file per session, in priority
> order \`.hermes.md\`/\`HERMES.md\` > \`AGENTS.md\` > \`CLAUDE.md\` — so without this file
> Hermes would never see Claude Code's operating rules or its accumulated project memory.
> This file mirrors both. Full architecture/conventions live in \`AGENTS.md\`; read it
> directly via the terminal tool for detail this digest omits. The living memory corpus
> (session history, decisions, incidents) lives outside this repo at
> \`${CLAUDE_MEMORY_DIR}/\` — same files Claude Code reads; read any of them directly for
> full context on an item mentioned only by title below.

---

## Claude Code operating core (mirrored — these rules apply to any agent working this repo, Hermes included)

${claudeCore}

---

## Live project memory index (mirrored from Claude Code's auto-memory, \`${CLAUDE_MEMORY_INDEX}\`)

${memoryIndex}
`;

  return truncateToBudget(header, CONTEXT_FILE_BUDGET);
}

function seedIfAbsent(path, content, label) {
  if (existsSync(path)) {
    console.log(`skip  ${label} — already exists (Hermes may have learned things since; not clobbering)`);
    return;
  }
  mkdirSync(HERMES_MEMORY_DIR, { recursive: true });
  writeFileSync(path, content, "utf8");
  console.log(`seed  ${label} — written (${content.length} chars)`);
}

const USER_MD_SEED = `§ Operator: SyniakSviatoslav, builds dowiz/DeliveryOS — a multi-tenant delivery/ordering platform (Rust/WASM sovereign core + Astro/React frontend + Supabase/Postgres).
§ Ask-don't-guess: on an ambiguous request, research first, then ask directly — never guess or act on a single statement without checking.
§ Wants every fix/feature validated with a falsifiable, deterministic proof — a red case and a green case, not a test that can't fail ("Verified-by-Math").
§ Ship discipline: feature branch → staging deploy → tests/Playwright proof → production only on explicit approval or merge to main.
§ Red-line areas (auth, money, RLS, DB migrations, bulk edits) need their own confirmation each time — a prior approval doesn't carry over.
`;

const MEMORY_MD_SEED = `§ Project: dowiz / DeliveryOS, repo root /root/dowiz. Full rules: .claude/CLAUDE.md (Claude Code operating core) and AGENTS.md (architecture/conventions) — read both via terminal at the start of work here.
§ Ethics charter (non-negotiable, every mode/agent): no AI for military or warfare; default to non-violent resolution; build toward peace and wellbeing for all; AI is a commons, never captured by a narrow group.
§ Red-line globs needing an explicit human/council gate before changing: auth, money, RLS policies, packages/db/migrations/, bulk-edit operations.
§ Mandatory Proof Rule / Verified-by-Math: every change needs a deterministic, falsifiable proof (an assertion/count/threshold that can go RED on bad input) — ship the RED case alongside the green one.
§ Full operational memory (active arcs, past decisions, incidents) lives at /root/.claude/projects/-root-dowiz/memory/ — same corpus Claude Code uses, indexed by MEMORY.md there. Read relevant files via terminal before assuming project state.
`;

writeFileSync(HERMES_MD_OUT, buildHermesMd(), "utf8");
console.log(`wrote ${HERMES_MD_OUT} (${buildHermesMd().length} chars)`);

seedIfAbsent(join(HERMES_MEMORY_DIR, "USER.md"), USER_MD_SEED, "~/.hermes/memories/USER.md");
seedIfAbsent(join(HERMES_MEMORY_DIR, "MEMORY.md"), MEMORY_MD_SEED, "~/.hermes/memories/MEMORY.md");
