#!/usr/bin/env node
// agent-health-pass — the harness measuring ITSELF (counsel Duty B, first implemented
// meta-loop P1 2026-07-02: docs/governance/ was mandated but never existed; every prior
// health signal was prose). Reads the gates' state, the advisory stores, the ledger, the
// loop registry, and the harness event log, and writes a dated report to docs/governance/.
//
// ADVISORY: always exits 0; hard failures belong to the guardrails (gate-armament,
// ledger-integrity, hook-matchers). WARN lines here feed the weekly librarian/retro run.
//
// Run: node scripts/agent-health-pass.mjs [--stdout]   (--stdout: print, don't write)
import { readFileSync, readdirSync, writeFileSync, existsSync, statSync, mkdirSync } from 'node:fs';
import { join } from 'node:path';

const ROOT = process.cwd();
const now = Date.now();
const today = new Date().toISOString().slice(0, 10);
const days = (ms) => Math.floor((now - ms) / 86400000);
const warns = [];
const lines = [];
const section = (t) => lines.push(`\n## ${t}\n`);
const row = (t) => lines.push(`- ${t}`);
const warn = (t) => { warns.push(t); lines.push(`- ⚠️ **${t}**`); };

lines.push(`# Agent-harness health pass — ${today}`);
lines.push(`\nGenerated by \`scripts/agent-health-pass.mjs\`. Advisory — deterministic gates decide.`);

// ── 1. Gates ────────────────────────────────────────────────────────────────
section('Gates (authority layer)');
const statePath = join(ROOT, '.claude/state');
const cleared = join(statePath, 'serious-cleared');
if (existsSync(cleared)) {
  const ls = readFileSync(cleared, 'utf8').split('\n').filter(Boolean);
  const nowS = Math.floor(now / 1000);
  const valid = ls.filter((l) => { const e = Number(l.split('|')[1]); return Number.isFinite(e) && e > nowS; });
  const legacy = ls.filter((l) => !/\|\d+$/.test(l.trim()));
  row(`serious-cleared: ${ls.length} line(s), ${valid.length} valid (unexpired), ${legacy.length} legacy/expiry-less.`);
  if (legacy.length) warn(`serious-cleared carries ${legacy.length} legacy line(s) without expiry — dead weight, truncate.`);
  if (ls.length > 5) warn(`serious-cleared has ${ls.length} lines — expired lines are accumulating; truncate periodically.`);
} else row('serious-cleared: absent (gate fully armed).');
for (const f of ['serious-override', 'redline-confirmed']) {
  const p = join(statePath, f);
  if (existsSync(p)) warn(`${f} exists (age ${days(statSync(p).mtimeMs)}d) — a standing human override; remove when done.`);
  else row(`${f}: absent (good).`);
}
const hooks = ['protect-paths.sh', 'serious-gate.sh', 'red-line-doubt-gate.sh', 'guard-bash.sh', 'pre-edit-lessons.sh', 'post-edit-gates.sh', 'loop-detector.sh', 'require-classification.sh', 'route-request.sh'];
const missing = hooks.filter((h) => !existsSync(join(ROOT, '.claude/hooks', h)));
if (missing.length) warn(`hook file(s) missing: ${missing.join(', ')}`);
else row(`all ${hooks.length} hook files present (registration is asserted by guardrail-hook-matchers; armament by guardrail-gate-armament).`);

// ── 2. Advisory stores ──────────────────────────────────────────────────────
section('Advisory stores (reflections / lessons)');
const inbox = join(ROOT, 'docs/reflections/INBOX');
if (existsSync(inbox)) {
  const files = readdirSync(inbox).filter((f) => f.endsWith('.md'));
  row(`INBOX: ${files.length} file(s).`);
  for (const f of files) {
    const p = join(inbox, f);
    const age = days(statSync(p).mtimeMs);
    if (age > 7) warn(`INBOX/${f} is ${age}d old — the retro/librarian drain is overdue.`);
    if (readFileSync(p, 'utf8').includes('fill in):')) warn(`INBOX/${f} has an UNFILLED WHY placeholder (blocks the Stop gate in hook v2).`);
  }
} else row('INBOX: absent.');
const lessonsDir = join(ROOT, 'docs/lessons');
if (existsSync(lessonsDir)) {
  const lessons = readdirSync(lessonsDir).filter((f) => f.endsWith('.md') && f !== 'INDEX.md');
  row(`lessons: ${lessons.length} file(s); INDEX rows: ${(readFileSync(join(lessonsDir, 'INDEX.md'), 'utf8').match(/^\|/gm) || []).length - 2}.`);
  if (lessons.length > 12) warn(`lesson store at ${lessons.length} — the librarian's promote-or-prune contract says it must not grow; schedule a prune.`);
}

// ── 3. Ledger ───────────────────────────────────────────────────────────────
section('Regression ledger');
const ledger = readFileSync(join(ROOT, 'docs/regressions/REGRESSION-LEDGER.md'), 'utf8');
const nums = [...ledger.matchAll(/^\|\s*(\d+[a-z]?)\s*\|/gm)].map((m) => m[1]);
row(`${nums.length} rows, max #${Math.max(...nums.filter((n) => /^\d+$/.test(n)).map(Number))} (uniqueness is enforced by guardrail-ledger-integrity).`);

// ── 4. Loops ────────────────────────────────────────────────────────────────
section('Loop system');
const regMd = readFileSync(join(ROOT, 'loops/registry.md'), 'utf8');
const mdIds = [...regMd.matchAll(/^\|\s*([a-z0-9-]+)\s*\|/gm)].map((m) => m[1]).filter((i) => i !== 'id');
const regJson = existsSync(join(ROOT, 'loops/runs/registry.json')) ? JSON.parse(readFileSync(join(ROOT, 'loops/runs/registry.json'), 'utf8')).loops.map((l) => l.id) : [];
row(`registry.md: ${mdIds.length} loops; runs/registry.json: ${regJson.length} (sync via loops-registry-sync).`);
const unrouted = mdIds.filter((i) => !regJson.includes(i));
if (unrouted.length) warn(`loop(s) invisible to the router: ${unrouted.join(', ')} — run loops-registry-sync.`);
const metricsPath = join(ROOT, 'loops/runs/metrics.jsonl');
if (existsSync(metricsPath)) {
  const rows_ = readFileSync(metricsPath, 'utf8').split('\n').filter(Boolean).map((l) => JSON.parse(l));
  const degenerate = rows_.filter((r) => !(r.cost_usd > 0) && !(r.tokens_out > 0)).length;
  row(`metrics.jsonl: ${rows_.length} run(s), ${degenerate} with zero cost/tokens (collector not wired for those runs).`);
  if (rows_.length < mdIds.length) warn(`only ${rows_.length} telemetry rows for ${mdIds.length} registered loops — most loop runs still bypass finalize.`);
} else warn('loops/runs/metrics.jsonl absent — no loop telemetry at all.');

// ── 5. Harness events ───────────────────────────────────────────────────────
section('Harness event log (hook v2 telemetry)');
const ev = join(ROOT, '.claude/logs/harness-events.jsonl');
if (existsSync(ev)) {
  const counts = {};
  for (const l of readFileSync(ev, 'utf8').split('\n').filter(Boolean)) {
    try { const d = JSON.parse(l); const k = `${d.hook}/${d.event}`; counts[k] = (counts[k] || 0) + 1; } catch { /* skip */ }
  }
  for (const [k, v] of Object.entries(counts).sort((a, b) => b[1] - a[1])) row(`${k}: ${v}`);
  const injects = Object.entries(counts).filter(([k]) => k.startsWith('pre-edit-lessons/')).reduce((s, [, v]) => s + v, 0);
  row(`lesson injections total: ${injects} — zero-hit lessons over a long window are prune candidates.`);
} else row('no harness-events.jsonl yet (hook v2 not applied, or nothing fired) — the advisory layer is still unmeasured.');

// ── Verdict ─────────────────────────────────────────────────────────────────
section('Verdict');
lines.push(warns.length === 0 ? '✅ **HEALTHY** — no warnings.' : `🟡 **${warns.length} warning(s)** — feed these to the weekly librarian/retro run:\n${warns.map((w) => `  1. ${w}`).join('\n')}`);

const report = lines.join('\n') + '\n';
if (process.argv.includes('--stdout')) { console.log(report); }
else {
  mkdirSync(join(ROOT, 'docs/governance'), { recursive: true });
  const out = join(ROOT, 'docs/governance', `agent-health-${today}.md`);
  writeFileSync(out, report);
  console.log(`✓ agent-health-pass: ${warns.length} warning(s) → ${out.replace(ROOT + '/', '')}`);
  for (const w of warns) console.log(`  ⚠ ${w}`);
}
